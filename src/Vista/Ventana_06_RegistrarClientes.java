    public void guardar() {
        if (!this.nombre.getText().equals(null)) {
            if (!this.apellido_paterno.getText().equals(null)) {
                if (!this.apellido_materno.equals(null)) {
                    if (!this.correo_electronico.getText().equals(null)) {
                        this.clientes.guardarCliente(nombre.getText(), apellido_paterno.getText(), apellido_materno.getText(), correo_electronico.getText());
                        Ventana_03_Menu menu = new Ventana_03_Menu();
                        menu.setVisible(true);
                        JOptionPane.showMessageDialog(rootPane, "Se ha guardado el cliente");
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Escriba el correo electronico");
                    }
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Escriba el apellido materno");
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Escriba el apellido paterno");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Escriba el nombre");
        }
    }
